import Web3 from "web3";
import { describe } from "mocha";
import { expect } from "chai";
import { HOST_WS_URL } from "../config";

const web3 = new Web3(HOST_WS_URL);
describe("Test contracts code", () => {
	it("The precompiles bytes code shouldn't be empty", async () => {
		[
			"0x0000000000000000000000000000000000000001",
			"0x0000000000000000000000000000000000000002",
			"0x0000000000000000000000000000000000000003",
			"0x0000000000000000000000000000000000000004",
			"0x0000000000000000000000000000000000000005",
			"0x0000000000000000000000000000000000000006",
			"0x0000000000000000000000000000000000000007",
			"0x0000000000000000000000000000000000000008",
			"0x0000000000000000000000000000000000000400",
			"0x0000000000000000000000000000000000000401",
			"0x0000000000000000000000000000000000000402",
			"0x0000000000000000000000000000000000000600",
			"0x0000000000000000000000000000000000000601",
			"0x0000000000000000000000000000000000000800",
		].forEach(async (x) => {
			const code = await web3.eth.getCode(x);
			expect(code).to.equal("0x60006000fd");
		});
	});
});
